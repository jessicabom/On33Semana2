#Aqui estamos trabalhando com funções, uma função nesse caso criado para calcular uma média de notas

def media(nota1, nota2):
    resultadoMedia = (nota1 + nota2) / 2
    print("Nota 1: ", nota1)
    print("Nota 2: ", nota2)
    print("O resultado da média é: ", resultadoMedia)
    
nota1 = 8
nota2 = 9

media(nota1, nota2)
    
